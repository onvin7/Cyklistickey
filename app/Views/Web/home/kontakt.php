    $(document).ready(function() {

        var parallax = -0.3;

        var $parallaxImages = $(".parallax-image");

        var original_offsets = [];

        $parallaxImages.each(function(i, el) {

            original_offsets.push($(el).offset().top);

        });



        $(window).scroll(function() {

            var dy = $(this).scrollTop();

            $parallaxImages.each(function(i, el) {

                var original_offset = original_offsets[i];

                $(el).css("top", (original_offset + dy * parallax) + "px");

            });

        });
        
        // Drag-scroll funkce pro galerii týmu
        const slider = document.querySelector('.galerie.team');
        let isDown = false;
        let startX;
        let scrollLeft;

        slider.addEventListener('mousedown', (e) => {
            isDown = true;
            slider.classList.add('active');
            startX = e.pageX - slider.offsetLeft;
            scrollLeft = slider.scrollLeft;
        });

        slider.addEventListener('mouseleave', () => {
            isDown = false;
            slider.classList.remove('active');
        });

        slider.addEventListener('mouseup', () => {
            isDown = false;
            slider.classList.remove('active');
        });

        slider.addEventListener('mousemove', (e) => {
            if (!isDown) return;
            e.preventDefault();
            const x = e.pageX - slider.offsetLeft;
            const walk = (x - startX) * 2; // Násobitel určuje rychlost scrollování
            slider.scrollLeft = scrollLeft - walk;
        });
    });



