        <p>3. K tomu, abychom mohli Smlouvu uzavřít, je třeba, abyste na E-shopu vytvořili návrh Objednávky. V tomto návrhu musí být uvedeny následující údaje:</p>
        
        <p>a) Informace o nakupovaném Zboží (na E-shopu označujete Zboží, o jehož nákup máte zájem, tlačítkem „Přidat do košíku");</p>
        
        <p>b) Informace o Ceně, Ceně za dopravu, způsobu platby Celkové ceny a požadovaném způsobu doručení Zboží; tyto informace budou zadány v rámci tvorby návrhu Objednávky v rámci uživatelského prostředí E-shopu, přičemž informace o Ceně, Ceně za dopravu a Celkové ceně budou uvedeny automaticky na základě Vámi zvolného Zboží a způsobu jeho doručení;</p>
        
        <p>c) Své identifikační a kontaktní údaje sloužící k tomu, abychom mohli doručit Zboží, zejména tedy jméno, příjmení, doručovací adresu, telefonní číslo a e-mailovou adresu;</p>
        
        <p>d) V případě Smlouvy, na základě které Vám budeme Zboží dodávat pravidelně a opakovaně, též informaci, jak dlouho Vám budeme Zboží dodávat.</p>
        
        <p>4. V průběhu tvorby návrhu Objednávky může až do doby jejího vytvoření údaje měnit a kontrolovat. Po provedení kontroly a potvrzení Objednávky Vám zašleme potvrzení o přijetí Objednávky na e-mailovou adresu, kterou jste uvedli při objednávce. Tímto potvrzením Objednávky se Smlouva považuje za uzavřenou.</p>
        
        <p>5. Pokud některé údaje v Objednávce nebudou správné nebo úplné, nebo pokud z jakéhokoli důvodu nebudeme moci Objednávku přijmout, budeme Vás o tom informovat prostřednictvím e-mailu nebo telefonu. V takovém případě Smlouva nebude uzavřena.</p>
        
        <h2>IV. CENA A PLATBA</h2>
        
        <p>1. Cena Zboží je uvedena na E-shopu včetně DPH. Cena za dopravu je uvedena samostatně a je součástí Celkové ceny.</p>
        
        <p>2. Celková cena je uvedena v českých korunách (CZK).</p>
        
        <p>3. Platbu Celkové ceny můžete provést následujícími způsoby:</p>
        
        <p>a) Platební kartou online prostřednictvím platební brány;</p>
        
        <p>b) Bankovním převodem na účet Prodávajícího;</p>
        
        <p>c) Hotově při převzetí Zboží (pouze v případě osobního odběru).</p>
        
        <p>4. V případě platby bankovním převodem je nutné provést platbu do 3 pracovních dnů od uzavření Smlouvy. Pokud platba nebude provedena v této lhůtě, máme právo Smlouvu zrušit.</p>
        
        <h2>V. DODÁNÍ ZBOŽÍ</h2>
        
        <p>1. Zboží Vám dodáme na adresu, kterou jste uvedli v Objednávce, nebo Vám umožníme osobní odběr na naší provozovně.</p>
        
        <p>2. Dodací lhůta je uvedena u každého Zboží na E-shopu. Pokud není dodací lhůta uvedena, zavazujeme se Zboží dodat do 30 dnů od uzavření Smlouvy.</p>
        
        <p>3. O odeslání Zboží Vás budeme informovat e-mailem na adresu, kterou jste uvedli v Objednávce.</p>
        
