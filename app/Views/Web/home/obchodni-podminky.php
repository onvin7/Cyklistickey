        <p>2. O odstoupení od Smlouvy nás informujte písemně na adresu Prodávajícího nebo elektronicky na e-mail jsem@cyklistickey.cz.</p>
        
        <p>3. Zboží nám zašlete nebo předáte bez zbytečného odkladu, nejpozději do 14 dnů od odstoupení od Smlouvy.</p>
        
        <p>4. Vrátíme Vám všechny peněžní prostředky, které jsme od Vás přijali, nejpozději do 14 dnů od odstoupení od Smlouvy.</p>
        
