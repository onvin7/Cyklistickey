        <p>a) Požadovat dodání nového Zboží bez vad, výměnu Zboží nebo opravu Zboží;</p>
        
        <p>b) Požadovat přiměřenou slevu z Ceny Zboží;</p>
        
        <p>c) Odstoupit od Smlouvy.</p>
        
        <p>3. Reklamaci můžete uplatnit písemně na adresu Prodávajícího nebo elektronicky na e-mail jsem@cyklistickey.cz. V reklamaci uveďte popis vady a způsob, jakým chcete reklamaci vyřídit.</p>
        
